// Get areas by city
export const getAreasByCity = (city) => {
  if (!city) {
    return [];
  }
  
  if (cityAreasData[city]) {
    return cityAreasData[city].areas;
  }
  
  // Return default areas if specific city data not available
  return cityAreasData["default"].areas;
};

// Get pincode by area
export const getPincodeByArea = (city, areaName) => {
  if (!city || !areaName) {
    return null;
  }
  
  const cityData = cityAreasData[city] || cityAreasData["default"];
  const area = cityData.areas.find(a => a.name === areaName);
  
  return area ? area.pincode : null;
};
